// Detect language
let currentLang = 'zh';
const path = window.location.pathname;
if (path.includes('/en/')) {
    currentLang = 'en';
} else if (path.includes('/es/')) {
    currentLang = 'es';
}

document.addEventListener('DOMContentLoaded', () => {
    const data = legData[currentLang];

    // Update title
    const title = document.getElementById('leg-title');
    if (title) title.textContent = data["leg-title"];

    // Update buttons
    const backp2Btn = document.getElementById('backp2-btn');
    const backhomeBtn = document.getElementById('backhome-btn');
    if (backp2Btn) backp2Btn.textContent = data["backp2-btn"];
    if (backhomeBtn) backhomeBtn.textContent = data["backhome-btn"];


    // Update content
    const contentDiv = document.getElementById('leg-content');
    if (contentDiv) {
        let html = '';

        // First 2 paragraphs
        for (let i = 0; i < 2; i++) {
            html += `<p class="page-paragraph">${data["leg-content"][i]}</p>`;
        }

        // Rest as list
        html += '<ul class="page-ul">';
        for (let i = 2; i < data["leg-content"].length; i++) {
            html += `<li class="page-li">${data["leg-content"][i]}</li>`;
        }
        html += '</ul>';

        contentDiv.innerHTML = html;
    }

    // Update footer
    const footer1 = document.getElementById('footer1');
    const footer2 = document.getElementById('footer2');
    if (footer1) footer1.textContent = data["copyright-text"][0];
    if (footer2) {
        footer2.innerHTML = `${data["copyright-text"][1].split("Tzu Chi Foundation.")[0]}<a href="https://go-vegetarian.tzuchi.org.tw/" target="_blank" rel="noopener noreferrer">Tzu Chi Foundation.</a>`;
    }
});
