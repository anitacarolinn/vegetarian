// Detect language
let currentLang = 'zh';
const path = window.location.pathname;
if (path.includes('/en/')) {
    currentLang = 'en';
} else if (path.includes('/es/')) {
    currentLang = 'es';
}

document.addEventListener('DOMContentLoaded', () => {
    const data = meatData[currentLang];

    // Update title
    const title = document.getElementById('meat-title');
    if (title) title.textContent = data["meat-title"];

    // Update buttons
    const backp2Btn = document.getElementById('backp2-btn');
    const backhomeBtn = document.getElementById('backhome-btn');
    if (backp2Btn) backp2Btn.textContent = data["backp2-btn"];
    if (backhomeBtn) backhomeBtn.textContent = data["backhome-btn"];


    // Update content
    const contentDiv = document.getElementById('meat-content');
    if (contentDiv) {
        let html = '';

        // First 3 paragraphs
        for (let i = 0; i < 3; i++) {
            html += `<p class="page-paragraph">${data["meat-content"][i]}</p>`;
        }

        // Middle items as list
        html += '<ul class="page-ul">';
        for (let i = 3; i < data["meat-content"].length - 3; i++) {
            html += `<li class="page-li">${data["meat-content"][i]}</li>`;
        }
        html += '</ul>';

        // Last 3 items inline
        html += '<ul class="page-ul-inline">';
        for (let i = data["meat-content"].length - 3; i < data["meat-content"].length; i++) {
            const item = data["meat-content"][i];
            if (item.includes('http')) {
                html += `<li class="page-li-inline"><a href="${item}" target="_blank" rel="noopener noreferrer">${item}</a></li>`;
            } else {
                html += `<li class="page-li-inline">${item}</li>`;
            }
        }
        html += '</ul>';

        contentDiv.innerHTML = html;
    }

    // Update footer
    const footer1 = document.getElementById('footer1');
    const footer2 = document.getElementById('footer2');
    if (footer1) footer1.textContent = data["copyright-text"][0];
    if (footer2) {
        footer2.innerHTML = `${data["copyright-text"][1].split("Tzu Chi Foundation.")[0]}<a href="https://go-vegetarian.tzuchi.org.tw/" target="_blank" rel="noopener noreferrer">Tzu Chi Foundation.</a>`;
    }
});
